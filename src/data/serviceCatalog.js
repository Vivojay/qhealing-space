export function durationBaseLabel(duration) {
  const base = String(duration || '').split('/')[0].trim();
  return base.replace(/\s+/g, ' ');
}

export function durationSortKey(label) {
  const cleaned = String(label || '').toLowerCase();
  const nums = cleaned.match(/\d+/g)?.map(Number) || [];
  const first = nums[0] || 9999;
  if (cleaned.includes('day')) return 10000 + first * 100;
  return first;
}

export function servicePriceLabel(service, billingProfile) {
  const serviceId = Number(service?.id);
  const lineItemOverride = BILLING_PRICE_BY_SERVICE_ID[billingProfile]?.[serviceId];
  if (lineItemOverride) return lineItemOverride;

  const bucket = durationBaseLabel(service?.duration);
  return (
    BILLING_PRICE_BY_DURATION[billingProfile]?.[bucket] ||
    BILLING_FALLBACK_LABEL[billingProfile] ||
    'Custom quote'
  );
}

export function hexToRgba(hex, alpha, fallback) {
  const raw = String(hex || '').trim();
  if (!raw.startsWith('#')) return fallback;
  const normalized =
    raw.length === 4 ? `#${raw[1]}${raw[1]}${raw[2]}${raw[2]}${raw[3]}${raw[3]}` : raw;
  if (!/^#[0-9a-fA-F]{6}$/.test(normalized)) return fallback;
  const r = parseInt(normalized.slice(1, 3), 16);
  const g = parseInt(normalized.slice(3, 5), 16);
  const b = parseInt(normalized.slice(5, 7), 16);
  return `rgba(${r}, ${g}, ${b}, ${alpha})`;
}

